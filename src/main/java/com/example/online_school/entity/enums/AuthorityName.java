package com.example.online_school.entity.enums;

/**
 * Enumeration representing different authority names.
 *
 * Перечисление, представляющее различные имена авторитетов.
 */
public enum AuthorityName {

    /**
     * Authority to create.
     *
     * Авторитет для создания.
     */
    CREATE,

    /**
     * Authority to read.
     *
     * Авторитет для чтения.
     */
    READ,

    /**
     * Authority to update.
     *
     * Авторитет для обновления.
     */
    UPDATE,

    /**
     * Authority to delete.
     *
     * Авторитет для удаления.
     */
    DELETE
}
