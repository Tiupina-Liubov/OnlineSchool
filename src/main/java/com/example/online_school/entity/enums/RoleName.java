package com.example.online_school.entity.enums;

/**
 * Enumeration representing different role names.
 *
 * Перечисление, представляющее различные имена ролей.
 */
public enum RoleName {

    /**
     * Role for regular users.
     *
     * Роль для обычных пользователей.
     */
    ROLE_USER,

    /**
     * Role for administrators.
     *
     * Роль для администраторов.
     */
   ROLE_ADMIN,

    /**
     * Role for teachers.
     *
     * Роль для учителей.
     */
    ROLE_TEACHER,

    /**
     * Role for class room teachers.
     *
     * Роль для классных руководителей.
     */
    ROLE_CLASS_ROOM_TEACHER,

    /**
     * Role for students.
     *
     * Роль для студентов.
     */
    ROLE_STUDENT,

    /**
     * Role for directors.
     *
     * Роль для директоров.
     */
    ROLE_DIRECTOR
}
